/**
 * Freshness-strip + /status generator — the data → site step for puxu.7.
 *
 * Two outputs, both self-contained HTML in the existing single-file pattern:
 *
 *   1. The landing page (`site/index.html`) gets the per-repo freshness +
 *      decision-mix strip injected at the TOP of `<main>`. The landing page is
 *      hand-maintained HTML, so we inject between two stable marker comments
 *      rather than regenerating the whole file — idempotent, and the rest of the
 *      page (council refusals, references) is untouched.
 *
 *   2. `site/status/index.html` — the `/status` USE-method view.
 *
 * Like `src/results/generate.ts`, the pure pieces (build strip, build use,
 * render, inject) are exported so tests assert structure + the C3 scanner can
 * run against the strings without touching disk.
 *
 * CURRENT STATE: the emit-evidence work across the 6 source repos is incomplete,
 * so the honest default has zero verified rows ⇒ every bucket is `no-data` ⇒ the
 * strip is (correctly) almost entirely loud-red. That is exactly the truth the
 * DR-035 C4 binding exists to surface.
 */

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import {
  buildFreshnessStrip,
  type FreshnessRowInput,
  type FreshnessStripView,
} from './bucket-model.js';
import {
  computeIngestUse,
  type IngestUseView,
  type RepoLiveness,
  type SupervisionPressure,
} from './use-model.js';
import { renderFreshnessStrip, renderStatusPage } from './render-strip.js';

/** Stable injection markers in the landing page. Content between is replaced. */
export const STRIP_MARKER_OPEN = '<!-- FRESHNESS-STRIP:START (generated by src/freshness) -->';
export const STRIP_MARKER_CLOSE = '<!-- FRESHNESS-STRIP:END -->';

/** A generated file: path under the site root → HTML content. */
export interface FreshnessGeneratedFile {
  readonly path: string;
  readonly html: string;
}

/** All inputs needed to build both surfaces for one render. */
export interface FreshnessInputs {
  /** Source repos in render order (the 6 ingest repos). */
  readonly repos: readonly string[];
  /** Verified gate-result rows across all repos (outside-window rows ignored). */
  readonly rows: readonly FreshnessRowInput[];
  /** Per-repo liveness for Utilization / Errors. */
  readonly liveness: readonly RepoLiveness[];
  /** Restart / escalation pressure for Saturation. */
  readonly pressure: SupervisionPressure;
  /** The render's "now" (RFC-3339). */
  readonly nowIso: string;
}

/** The built views (strip + USE), exposed for tests + render. */
export interface FreshnessBuild {
  readonly strip: FreshnessStripView;
  readonly use: IngestUseView;
}

/** Build the strip + USE views from raw inputs (pure). */
export function buildFreshness(inputs: FreshnessInputs): FreshnessBuild {
  const strip = buildFreshnessStrip(inputs.repos, inputs.rows, inputs.nowIso);
  const use = computeIngestUse(inputs.liveness, inputs.pressure, strip, inputs.nowIso);
  return { strip, use };
}

/**
 * Inject the rendered strip fragment into a landing-page HTML string between the
 * two markers. Idempotent: re-running replaces the prior strip in place.
 *
 * Throws if the markers are absent — that means the landing page lost its
 * injection region and we must NOT silently append (which would render the strip
 * in the wrong place or duplicate it).
 */
export function injectStrip(landingHtml: string, stripFragment: string): string {
  const start = landingHtml.indexOf(STRIP_MARKER_OPEN);
  const end = landingHtml.indexOf(STRIP_MARKER_CLOSE);
  if (start === -1 || end === -1 || end < start) {
    throw new Error(
      `freshness strip markers not found in landing page (expected ${STRIP_MARKER_OPEN} … ${STRIP_MARKER_CLOSE})`,
    );
  }
  const before = landingHtml.slice(0, start + STRIP_MARKER_OPEN.length);
  const after = landingHtml.slice(end);
  return `${before}\n${stripFragment}\n        ${after}`;
}

/**
 * Build the generated-file map (without touching disk).
 *
 * Returns:
 *   - `index.html`        — the landing page with the strip injected
 *   - `status/index.html` — the `/status` USE-method page
 *
 * `landingHtml` is the current landing-page source (read by the caller); we
 * inject into it so the file map is fully deterministic for tests + the C3 scan.
 */
export function generateFreshnessFiles(
  build: FreshnessBuild,
  landingHtml: string,
): FreshnessGeneratedFile[] {
  const stripFragment = renderFreshnessStrip(build.strip);
  const injected = injectStrip(landingHtml, stripFragment);
  const statusHtml = renderStatusPage(build.use, build.strip);
  return [
    { path: 'index.html', html: injected },
    { path: 'status/index.html', html: statusHtml },
  ];
}

/** Write the generated files under `siteRoot`. Returns absolute paths written. */
export async function writeFreshnessSite(
  files: readonly FreshnessGeneratedFile[],
  siteRoot: string,
): Promise<string[]> {
  const written: string[] = [];
  for (const file of files) {
    const abs = join(siteRoot, file.path);
    await mkdir(dirname(abs), { recursive: true });
    await writeFile(abs, file.html, 'utf8');
    written.push(abs);
  }
  return written;
}

/**
 * End-to-end: read the landing page from `siteRoot`, inject the strip, write
 * both files back. Convenience wrapper for the CLI entrypoint.
 */
export async function generateAndWrite(
  inputs: FreshnessInputs,
  siteRoot: string,
): Promise<string[]> {
  const build = buildFreshness(inputs);
  const landingPath = join(siteRoot, 'index.html');
  const landingHtml = await readFile(landingPath, 'utf8');
  const files = generateFreshnessFiles(build, landingHtml);
  return writeFreshnessSite(files, siteRoot);
}
